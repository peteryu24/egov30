package gmx.fwd.vo.mapvo;

public class LayerStyleVO {
	
	private int id;
	private String attributeName;
	private String strokeColor;
	private int strokeWidth;
	private String fillColor;
	private String font;
	private String textColor;
	private String textStrokeColor;
	private int textStrokeWidth;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getAttributeName() {
		return attributeName;
	}

	public void setAttributeName(String attributeName) {
		this.attributeName = attributeName;
	}

	public String getStrokeColor() {
		return strokeColor;
	}

	public void setStrokeColor(String strokeColor) {
		this.strokeColor = strokeColor;
	}

	public int getStrokeWidth() {
		return strokeWidth;
	}

	public void setStrokeWidth(int strokeWidth) {
		this.strokeWidth = strokeWidth;
	}

	public String getFillColor() {
		return fillColor;
	}

	public void setFillColor(String fillColor) {
		this.fillColor = fillColor;
	}

	public String getFont() {
		return font;
	}

	public void setFont(String font) {
		this.font = font;
	}

	public String getTextStrokeColor() {
		return textStrokeColor;
	}

	public void setTextStrokeColor(String textStrokeColor) {
		this.textStrokeColor = textStrokeColor;
	}

	public String getTextColor() {
		return textColor;
	}

	public void setTextColor(String textColor) {
		this.textColor = textColor;
	}

	public int getTextStrokeWidth() {
		return textStrokeWidth;
	}

	public void setTextStrokeWidth(int textStrokeWidth) {
		this.textStrokeWidth = textStrokeWidth;
	}
}
